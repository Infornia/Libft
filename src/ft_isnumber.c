#include "libft.h"

int		ft_isnumber(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if ((i == 0 && (str[i] == '-' || str[i] == '+')) || ft_isdigit(str[i]))
			i++;
		else
			return (0);
	}
	return (1);
}
